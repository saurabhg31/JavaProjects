    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
int n,se=0,so=0,ctr=1;
n=Integer.parseInt(jTextField1.getText());
while(ctr<=n){
    if(ctr%2==0)
        se+=ctr;
    else
        so+=ctr;
    ctr++;
}
System.out.println("Sum of even integers = "+se);
System.out.println("Sum of odd integers = "+so);
System.out.println("Total sum of all the integers = "+(se+so));
JOptionPane.showMessageDialog(null, "The sum of even integers is "+se);
JOptionPane.showMessageDialog(null, "The sum of odd integers is "+so);
    }//GEN-LAST:event_jButton1ActionPerformed

